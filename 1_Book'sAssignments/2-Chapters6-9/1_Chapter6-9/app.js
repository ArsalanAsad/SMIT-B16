var a = 10

document.write("The valueof a is: " + a + "<br>");
document.write("-------------------------------<br>");

//Prefix Increment
document.write("The value of ++a is: " + (++a) + "<br>");
document.write("Now the value of a is: " + a + "<br><br>");

//Suffix Increment
document.write("The value of a++ is: " + (a++) + "<br>");
document.write("Now the value of a is: " + a + "<br><br>");

//Prefix Decrement
document.write("The value of --a " + (--a) + "<br>");
document.write("Now the value of a is " + a + "<br><br>");

//Suffix Decrement
document.write("The value of a-- is: " + (a--) + "<br>")
document.write("Now the value of a is; " + a + "<br><br>");